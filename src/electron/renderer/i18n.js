  function localeFromTag(value) {
    const tag = String(value || '').replace(/_/g, '-').trim().toLowerCase();
    if (!tag) return null;
    if (tag === 'zh') return 'zh-TW';
    if (tag.startsWith('zh-hant') || /-(tw|hk|mo)\b/.test(tag)) return 'zh-TW';
    if (tag.startsWith('zh-hans') || /-(cn|sg|my)\b/.test(tag)) return 'zh-CN';
    if (tag === 'zh-tw') return 'zh-TW';
    if (tag === 'zh-cn') return 'zh-CN';
    if (tag === 'ko' || tag.startsWith('ko-') || tag === 'kr') return 'ko';
    if (tag === 'ja' || tag.startsWith('ja-')) return 'ja';
    if (tag === 'en' || tag.startsWith('en-')) return 'en';
    return null;
  }

  function normalizeLanguage(value) {
    const raw = String(value || '').replace(/_/g, '-').trim();
    const lower = raw.toLowerCase();
    if (lower === 'auto') return 'auto';
    return localeFromTag(raw) || 'auto';
  }

  function resolveLocale(language, preferredLanguages) {
    const normalized = normalizeLanguage(language);
    if (normalized !== 'auto') return normalized;
    const languages = Array.isArray(preferredLanguages) ? preferredLanguages : [preferredLanguages];
    for (const item of languages) {
      const locale = localeFromTag(item);
      if (locale) return locale;
    }
    return 'en';
  }

  function translate(locale, key, params = {}) {
    const resolved = normalizeLanguage(locale) === 'auto' ? 'en' : normalizeLanguage(locale);
    const template = MESSAGES[resolved]?.[key] ?? MESSAGES.en[key] ?? key;
    return String(template).replace(/\{([a-zA-Z0-9_]+)\}/g, (_match, name) => {
      return params[name] === undefined || params[name] === null ? '' : String(params[name]);
    });
  }

  function translated(locale, key) {
    return translate(locale, key);
  }

  function applyTranslations(root, locale) {
    const scope = root || (typeof document !== 'undefined' ? document : null);
    if (!scope || typeof scope.querySelectorAll !== 'function') return locale;
    const resolved = normalizeLanguage(locale) === 'auto' ? 'en' : normalizeLanguage(locale);
    const docEl = scope.documentElement || scope.ownerDocument?.documentElement;
    if (docEl?.setAttribute) docEl.setAttribute('lang', resolved);

    for (const el of scope.querySelectorAll('[data-i18n]')) {
      el.textContent = translated(resolved, el.dataset.i18n);
    }
    for (const el of scope.querySelectorAll('[data-i18n-title]')) {
      const text = translated(resolved, el.dataset.i18nTitle);
      el.title = text;
      if (el.setAttribute) el.setAttribute('title', text);
    }
    for (const el of scope.querySelectorAll('[data-i18n-aria-label]')) {
      const text = translated(resolved, el.dataset.i18nAriaLabel);
      if (el.setAttribute) el.setAttribute('aria-label', text);
    }
    for (const el of scope.querySelectorAll('[data-i18n-placeholder]')) {
      const text = translated(resolved, el.dataset.i18nPlaceholder);
      el.placeholder = text;
      if (el.setAttribute) el.setAttribute('placeholder', text);
    }
    return resolved;
  }

  return { LANGUAGE_OPTIONS, MESSAGES, applyTranslations, normalizeLanguage, resolveLocale, translate };
